"""Dataclasses for the knowledge base, ideas, themes, changelog, and run state.

Serialization is deliberately simple: ``asdict`` for writing and ``from_dict``
(which ignores unknown keys and fills missing ones with defaults) for reading.
This keeps the committed JSON forward/backward compatible as the schema evolves.
"""

from __future__ import annotations

from dataclasses import asdict, dataclass, field, fields
from typing import Any, TypeVar

KB_SCHEMA_VERSION = 1

T = TypeVar("T")


def _from_dict(cls: type[T], data: dict[str, Any]) -> T:
    """Construct a dataclass from a dict, ignoring unknown keys."""
    known = {f.name for f in fields(cls)}  # type: ignore[arg-type]
    return cls(**{k: v for k, v in data.items() if k in known})  # type: ignore[call-arg]


